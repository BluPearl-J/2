PI = 3.14159 ;

radius = 2;

diameter = 2 * radius ;

print ("The diameter of the circle with radius 2 is",diameter );

circumference = 2 * PI * radius;

print ("The circumference of the circle with radius 2 is",circumference);

area = PI * radius * radius;


print ("The area of the circle with radius 2 is",area);
